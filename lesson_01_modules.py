# Lesson 1: Python Modules & Webbrowser Automation
# A module is a Python file containing reusable code (functions, classes, variables).
import webbrowser

# Using the dot operator to access open() function in webbrowser module.
# This opens the default web browser to the specified URL.
webbrowser.open("https://www.youtube.com")

# Key concepts:
# - webbrowser is a built-in module (no pip install required).
# - webbrowser.open(url) takes the URL string as an argument.
